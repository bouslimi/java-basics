package com.trigger_soft.controlstructures;

public class IfElse {

    public static void main(String[] args) {
        int n = 10;
        //int n = 0;
        //int n = -5;

        if (n == 0) {
            System.out.println("Zero!");
        } else if (n > 0) {
            System.out.println("Positive number!");
        } else {
            System.out.println("Negative number!");
        }
    }
}
